export function changeRect(id, doc)
{
    var url;
    var change = {
        url,
        id
    }

    //0702004220633
    let state = false;
    list.forEach((item) =>
    {
        let findID = item.toString().split('/')[3].split('.')[0];
        if (id == findID) state = true;
    });
    
    if (!state) return 0;

    if (doc.getElementById(id) == null)
    {
        //alert('Не найден');
        //var lastSymb = id[id.length-1]; //Последний символ
        var truncWord = id.substring(0, id.length - 1); //Всё, кроме последнего символа

        var checkDoc = "";

        for (var i = 1; i <= 3; i++)
        {
            list.forEach((item) => 
            {       
                if (doc.getElementById(truncWord + i.toString()))
                {
                    checkDoc = truncWord + i.toString();
                }
            });
        }

        // console.log("Элемент, на который заменяется: " + id);
        // console.log("Элемент, который нужно заменить: " + checkDoc);

        list.forEach((item) => 
        {
            let findID = item.toString().split('/')[3].split('.')[0];
            if (findID == id)
            {
                change.url = item;
            }
        });

        change.id = checkDoc;
        
    }else
    {
       // alert('Найден');
       change.url = 0;
       change.id = "0";
    }
    return change;
}